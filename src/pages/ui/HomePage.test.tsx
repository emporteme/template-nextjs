describe("Math operations", () => {
    test("adds 1 + 2 to equal 3", () => {
      expect(1 + 2).toBe(3)
    })
  
    test("multiplies 2 * 3 to equal 6", () => {
      expect(2 * 3).toBe(6)
    })
  
    test("subtracts 5 - 2 to equal 3", () => {
      expect(5 - 2).toBe(3)
    })
  
    test("divides 6 / 2 to equal 3", () => {
      expect(6 / 2).toBe(3)
    })
  })
  